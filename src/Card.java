public class Card {
    String nameCard;
    String elemen;
    int hp;
    Boolean isActive;
    String inisial;
    int skillPoint;

    public Card(String nameCard, String elemen, int hp, Boolean isActive, String inisial, int skillPoint) {
        this.nameCard = nameCard;
        this.elemen = elemen;
        this.hp = hp;
        this.isActive = isActive;
        this.inisial = inisial;
        this.skillPoint = skillPoint;
    }
}
